package net.peterkuterna.android.apps.swipeytabs;

import android.os.Bundle;
import android.support.v4.app.Fragment;
import android.view.LayoutInflater;
import android.view.View;
import android.view.ViewGroup;
import android.widget.TextView;
import ca.germuth.puzzled.R;

public abstract class SwipeyTabFragment extends Fragment {

	@Override
	public View onCreateView(LayoutInflater inflater, ViewGroup container,
			Bundle savedInstanceState) {
		ViewGroup root = (ViewGroup) inflater.inflate(
				R.layout.fragment_swipeytab, null);
		//final String title = getArguments().getString("title");
		final String title = this.getName();
		((TextView) root.findViewById(R.id.text)).setText(title);
		return root;
	}
	
	
	public abstract String getName(); 
}
